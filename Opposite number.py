"""
Very simple, given a number, find its opposite.

Examples:
1: -1
14: -14
-34: 34
"""

def opposite(number):
    return number * -1

print('Tests:')
print(opposite(1))
print(opposite(25.6))
print(opposite(0))
print(opposite(1425.2222))
print(opposite(-3.1458))
print(opposite(-95858588225))